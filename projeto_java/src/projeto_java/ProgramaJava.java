package projeto_java;

public class ProgramaJava {

	public static void main(String[] args) {

		System.out.println("Boas Práticas de Programação");
		System.out.println("Com Tipos de Variáveis");

		int laranja = 10;
		int abacate = 15;

		int morango = 50;
		int melancia = 100;
		
		int ramutan = 20;
		int janbo = 18;

		int resultado = laranja + abacate;
		int resultado2 = morango * melancia;
		int resultado3 = ramutan - janbo;

		int resultado4 = (laranja + abacate) / 2;

		System.out.println("Primeira linha teste1");
		System.out.println("Segunda linha teste2");
		System.out.println("Terceira linha teste3");

		System.out.println("Resultado da Soma das Frutas foram: " + resultado);
		System.out.println("Resultado da Multiplicação Frutas foram: " + resultado2);
		System.out.println("Resultado da Diminuição das Frutas foram: " + resultado3);
		System.out.println("Resultado da Divisão das Frutas foram: " + resultado4);

	}

}
